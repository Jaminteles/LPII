class operacao {

    public int a;
    public int b;
    
    int somar(int a, int b) {
        return a + b;
    }
    int sub(int a, int b) {
        return a - b;
    }
    int multi(int a, int b) {
        return a * b;
    }
    int div(int a, int b) {
        return a / b;
    }
}